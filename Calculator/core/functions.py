from math import *

def square(x):
    return x**2

def cube(x):
    return x**3

def inverse(x):
    return 1/x

def square_root(x):
    return sqrt(x)

def cube_root(x):
    return x**(1/3)

def absolute(x):
    return abs(x)

def ten_power(x):
    return 10**x

def two_power(x):
    return 2**x

def natural_log(x):
    return log(x)

def common_log(x):
    return log10(x)

def exponent(x):
    return exp(x)

def factorial_num(x):
    return factorial(int(x))

def sine(x):
    return sin(radians(x))

def cosine(x):
    return cos(radians(x))

def tangent(x):
    return tan(radians(x))

def cotangent(x):
    return 1/tan(radians(x))